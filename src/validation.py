class Validation(object):
    '''
    classdocs
    '''


    def __init__(self):
        '''
        Constructor
        '''
        
    @classmethod
    def isNumeric(self, suspect):
        if suspect == 1:
            return True

    @staticmethod
    def isTel(suspect):
        if suspect == 2:
            return True